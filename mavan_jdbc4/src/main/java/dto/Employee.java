package dto;

public class Employee {

	private int empId;
	private String empName;
	private int empAge;
	private int empDeptno;
	private long empPhno;
	private double empSalery;
	private String empLocation;
	
	
	
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public int getEmpAge() {
		return empAge;
	}
	public void setEmpAge(int empAge) {
		this.empAge = empAge;
	}
	public int getEmpDeptno() {
		return empDeptno;
	}
	public void setEmpDeptno(int empDeptno) {
		this.empDeptno = empDeptno;
	}
	public long getEmpPhno() {
		return empPhno;
	}
	public void setEmpPhno(long empPhno) {
		this.empPhno = empPhno;
	}
	public double getEmpSalery() {
		return empSalery;
	}
	public void setEmpSalery(double empSalery) {
		this.empSalery = empSalery;
	}
	public String getEmpLocation() {
		return empLocation;
	}
	public void setEmpLocation(String empLocation) {
		this.empLocation = empLocation;
	}
	
	
	
}
